st.subheader("🔍 模型簡介")
st.markdown("""
本預測工具採用 **Log-Normal AFT（加速失敗時間）模型**，依據過往 2020 與 2024 年柔道國際賽事資料建立。  
模型的目的是根據比賽條件，預測選手在特定時間點仍在場上或已經獲勝的機率。

- `Survival Probability`（場上機率）：選手在某個時間點仍未結束比賽（還在場上）的機率。
- `End Probability`（獲勝機率）：代表選手在該秒數**之前已經獲勝**（例如 Ippon 或比賽結束）的機率。
- 模型考慮各種比賽情境條件，並建立統計意義上的生存曲線。

> 📌 **提醒**：這是一種機率預測模型，結果僅反映資料趨勢，並非個人命運決定 😎  
若你對預測有意見，請找裁判，不要找我 XD
""")

st.markdown("---")
st.subheader("🧬 使用的變數一覽")
st.markdown("""
模型會根據以下變數作出預測，這些變數來自每場比賽紀錄中雙方選手的狀況：

| 變數名稱             | 說明                                             |
|----------------------|--------------------------------------------------|
| 比賽性別（Gender）   | 男 / 女選手                                     |
| 重量級別（Weight）   | 依據選手性別與量級編碼（例如 Men -73kg）       |
| 指導次數（Shido）    | 得勝方獲得指導（Shido）的次數（0~2）           |
| 技有（Waza-ari）     | 得勝方是否取得技有（0: 無, 1: 有）              |
| 世界排名差（Ranking Difference） | 得勝方排名 - 對手排名                       |
| 比賽年份（Year）     | 2020 或 2024                                     |
| 是否黃金得分（GS）   | 是否進入 Golden Score（延長賽）                 |
""")

